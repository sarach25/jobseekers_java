    private void publierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_publierActionPerformed

       DateFormat df = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");
        Date today = Calendar.getInstance().getTime();
        String msg = df.format(today);
        afficherdate.setText(msg);
        
       // ImageIcon i=new ImageIcon(urlimg.getText());
        ImageIcon i = new ImageIcon(new ImageIcon(urlimg.getText()).getImage().getScaledInstance(200, 200, Image.SCALE_DEFAULT));
        
       
        
        
        jimage.setIcon(i);
        
        publication p=new publication();
        p.setContenu(pub.getText());
        p.setDate(msg);
        p.setTypepub(typepub.getText());
        p.setEtatpub(etatpub.getText());
       
        p.setUrlimg(urlimg.getText());
        
        
        
        publicationDAO pdao = new publicationDAO();
        pdao.add(p);
        jpub.setText(pub.getText());
        
        
        

    }//GEN-LAST:event_publierActionPerformed

